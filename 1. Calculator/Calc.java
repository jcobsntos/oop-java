package week2;

public class Calc {
	static int add(int x, int y) {
		return x+y;
	}
	static int diff(int x, int y) {
		return x-y;
	}
	static int mult(int x, int y) {
		return x*y;
	}
	static float div(int x, int y) {
		return x/y;
	}
	static float mod(int x, int y) {
		return x%y;
	}
	static double abs(int x, int y) {
		return Math.abs(x-y);
	}
}
